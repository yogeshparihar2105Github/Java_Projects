public class PrePostIncrement {
    public static void main(String[] args) {
        
        int a = 5;
        
        int b = ++a;
        System.out.println("This is b in which there is ++a And a is 5 bydefault : " + b);

        int c = 5;
        
        int d = c++;
        System.out.println("This is c in which there is c++ And c is 5 bydefault : " + d);
        
        
    }
}
