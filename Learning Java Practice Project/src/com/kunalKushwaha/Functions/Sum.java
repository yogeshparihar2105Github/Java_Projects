package com.kunalKushwaha.Functions;

public class Sum {
    public static void main(String[] args) {
        System.out.println(sum(2, 3));
        System.out.println(product(2, 3));
    }
    static int sum(int a , int b ){
        return a+b;
    }
    static int product(int a , int b ){
        return a*b;
    }
}
